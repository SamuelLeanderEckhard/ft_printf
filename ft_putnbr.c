#include "ft_printf.h"

int	ft_putnbr(int n)
{
	long int	i;

	i = n;
	if (n < 0)
	{
		ft_putchar('-');
		i = -n;
	}
	if (i <= 9)
	{
		ft_putchar(i + 48);
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
}
